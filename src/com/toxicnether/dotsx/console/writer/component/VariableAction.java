package com.toxicnether.dotsx.console.writer.component;

/**
 * 
 * Esta interfaz permite ser utilizada para manejar
 * las acciones del cambio de Variables para el contexto
 * consola.
 * 
 * @author Abraham Lora
 *
 */
public interface VariableAction {

	/**
	 * 
	 * Este metodo es disparado cuando la variable se
	 * cambia de valor sin ser cambiada de manera
	 * silenciosa.
	 * 
	 * @param value El nuevo valor de la variable.
	 */
	public void change(String value);
	
}
