package com.toxicnether.dotsx.console.writer.timelines.type;

import java.util.LinkedList;

public enum GameTimelineType {

	UNKNOW,
	ORIGINAL,
	X,
	BOMBER,
	
	;
	
	public static GameTimelineType getType(LinkedList<String> state) {
		
		for(String line : state)
			if(line.startsWith("MODO: "))
				for(GameTimelineType type : GameTimelineType.values())
					if(type.name().equalsIgnoreCase(line.replaceAll("MODO: ", "")))
						return type;
		
		return GameTimelineType.UNKNOW;
		
	}
	
}
