package com.toxicnether.dotsx.core.config.key;

public enum KeyType {

	Q,
	W,
	E,
	R,
	T,
	Y,
	U,
	I,
	O,
	P,
	A,
	S,
	D,
	F,
	G,
	H,
	J,
	K,
	L,
	Z,
	X,
	C,
	V,
	B,
	N,
	M,
	
	;
	
	public static boolean isValidHotkey(String input) {
		
		for(KeyType type : KeyType.values())
			if(type.name().equalsIgnoreCase(input))
				return true;
		
		return false;
		
	}
	
}
